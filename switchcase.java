class Switchcase
{
public static void main (String[] args)
{
int choice=6;

switch(choice)
{
case 1:
       System.out.println("value is 1");
       break;
case 2:
       System.out.println("value is 2");
       break;
case 3:
       System.out.println("value is 3");
       break;
case 4:
       System.out.println("value is 4");
       break;

default:
       System.out.println("Invalid");
       break;

}
}
}